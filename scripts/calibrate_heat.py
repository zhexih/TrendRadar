def heat_score(rank):
    if rank <= 3: return 10
    if rank <= 10: return 6
    if rank <= 20: return 3
    return 1

print(f"{'品类':12s} {'得分':>5s} {'命中':>5s} {'分布'}")
print('-' * 70)

scores = []
for cat, keywords in categories_kw.items():
    total = 0
    count = 0
    dist = []
    for kw in keywords:
        cur.execute('SELECT rank FROM news_items WHERE title LIKE ?', (f'%{kw}%',))
        for (r,) in cur.fetchall():
            total += heat_score(r)
            count += 1
            dist.append(str(r))
    dist_str = ','.join(dist[:10]) + ('...' if len(dist) > 10 else '')
    bar = '█' * (total // 3)
    print(f'{cat:12s} {total:>5d} {count:>5d} {bar} R={dist_str}')
    scores.append(total)

print()
scores.sort(reverse=True)
print(f'最高分: {scores[0]}, P90: ~{scores[len(scores)//10]}, 中位数: ~{scores[len(scores)//2]}, 最低: {scores[-1]}')

